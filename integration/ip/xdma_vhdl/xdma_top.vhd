-- (DMA/Bridge Subsystem for PCI Express) and integrates it with a local 4KB
-- dual-port Block RAM (BRAM). This provides an end-to-end verifiable loopback
-- interface where the host PC can write and read from BRAM over PCIe JTAG/DMA.
--
-- Dependencies: 
-- - my_xdma_core: Generated PCIe XDMA Core IP
-- - IBUFDS_GTE2: Xilinx primitive for differential MGT reference clocks
