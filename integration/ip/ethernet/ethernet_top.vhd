-- Description: 
-- This is a top-level VHDL design that instantiates the Xilinx Tri-Mode
-- Ethernet MAC (TEMAC) IP and configures it in an end-to-end local digital loopback.
-- Packets received over the RGMII interface are deserialized, parsed by the MAC,
-- outputted on the rx_axis (AXI-Stream) interface, and immediately looped back
-- into the tx_axis (AXI-Stream) interface to be re-transmitted over RGMII.
--
-- Dependencies: 
-- - my_temac_core: Generated Tri-Mode Ethernet MAC IP
-- - PLLE2_BASE: Xilinx PLL clock generator primitive
